package sample_application;

public class ArithmaticProblem3 {

	public static void main(String[] args) {
		
		int celsius = 30; // 섭씨 30도.
		int farenheit = (celsius * 9 / 5) + 32; // 화씨, ( 섭씨 * 9 / 5) + 32
		
		System.out.println("섭씨 " + celsius + "도는 화씨 " 
						+ farenheit + "도 입니다.");
		
	}
	
}
